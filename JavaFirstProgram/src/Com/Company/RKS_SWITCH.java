package Com.Company;

public class RKS_SWITCH {
    public static void main(String[] args) {
        //Conditional Statement
        int i=5;
        switch (i){
            case 1: System.out.println("Code1");
            break;
            case 5: System.out.println("code5");
            break;
            default: System.out.println("code7");
        }
        // if condition
        if (i==1){
            System.out.println("code");
        }else if (i==5){
            System.out.println("code5");
        }else {
            System.out.println("code7");
        }

    }
}
