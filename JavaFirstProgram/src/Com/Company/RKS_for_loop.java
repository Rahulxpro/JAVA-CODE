public class RKS_for_loop {
    public static void main(String[] args) {
        //  Q =  Get the square of the 5 integers
        //using loop
        // for (<initialization>; <countotation test>; <update>)
        for (int i=1; i<=5; i++){
            System.out.println(i + "  Squared = "+ (i*i) );
        }
    }
}
